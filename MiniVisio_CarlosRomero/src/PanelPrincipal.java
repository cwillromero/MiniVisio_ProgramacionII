    private void jmiCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiCodigoActionPerformed
        if (centinela == 2) {
            taCodigo.setText("");
            String c = "";
            if (centinela == 2) {
                for (int i = 0; i < clases.size(); i++) {
                    CodigoDiagramaDeClases cod = new CodigoDiagramaDeClases((JTree) clases.get(i));
                    cod.Inicio();
                    cod.Atributos();
                    cod.Constructor();
                    cod.Metodo();
                    cod.Destructores();
                    cod.Final();
                    c += cod.getCodigo();
                }
                taCodigo.setText(c);
            }
            jPanel1.setBackground(Color.BLACK);
            jdCodigo.setModal(true);
            jdCodigo.pack();
            jdCodigo.setLocationRelativeTo(this);
            jdCodigo.setVisible(true);
        } else if (centinela == 1) {
            try {
                taCodigo.setText("");
                String c = "";
                CodigoDiagramaDeFlujo cod = new CodigoDiagramaDeFlujo();
                cod.Inicio();
                for (int i = 0; i < componentes.size(); i++) {
                    cod.setObjeto(componentes.get(i));
                    cod.Generar();
                }
                cod.Final();
                c = cod.getCodigo();
                if (cod.isX() == true) {
                    taCodigo.setText(c);
                    jPanel1.setBackground(Color.BLACK);
                    jdCodigo.setModal(true);
                    jdCodigo.pack();
                    jdCodigo.setLocationRelativeTo(this);
                    jdCodigo.setVisible(true);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ocurrió un Error! \nEs probable que usted haya Ingresado mal un Dato.\nFavor Revisar el Diagrama", "Error", 0);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jmiCodigoActionPerformed

